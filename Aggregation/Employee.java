package Aggregation;

public class Employee {
    private int id;
    private String name;
    private int salary;
    Car c;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public Car getC() {
        return c;
    }

    public void setC(Car c) {
        this.c = c;
    }
}
